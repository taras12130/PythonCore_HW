# Define variables a and b. Read values a and b from console and calculate: 
# a + b 
# a - b 
# a * b 
# a / b
# a**b.
# Output obtained results.

a = int(input("Enter a: "))
b = int(input("Enter b: "))

print(f"a+b={a+b}")
print(f"a-b={a-b}")
print(f"a*b={a*b}")
print(f"a/b={round(a/b,2)}")
print(f"a**b={a**b}")